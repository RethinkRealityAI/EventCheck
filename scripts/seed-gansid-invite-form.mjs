// scripts/seed-gansid-invite-form.mjs
//
// Generates supabase/seeds/gansid-invite-form.sql — an idempotent upsert of the
// "GANSID Congress 2026 — Free Registration" form used by the bulk-contact
// invite path. It clones the LIVE gansid-congress-2026 form's fields, drops the
// registration-type / speaker / ticket fields, and flips everything optional
// except the three required consents. Run once when the source form's fields
// change; commit the generated .sql.
//
// Usage: SVC_KEY=<GANSID service_role key> node scripts/seed-gansid-invite-form.mjs
import fs from 'node:fs';

const GAN = 'https://gticuvgclbvhwvpzkuez.supabase.co';
const SVC = process.env.SVC_KEY;
if (!SVC) { console.error('Set SVC_KEY (GANSID service_role key)'); process.exit(1); }

const DROP = new Set(['f_mode', 'f_present', 'f_ticket']);
const KEEP_REQUIRED = new Set(['f_consent_photo', 'f_consent_terms', 'f_consent_liability']);

const r = await fetch(`${GAN}/rest/v1/forms?id=eq.gansid-congress-2026&select=fields`, {
  headers: { apikey: SVC, Authorization: `Bearer ${SVC}` },
});
if (!r.ok) { console.error('fetch failed', r.status, await r.text()); process.exit(1); }
const rows = await r.json();
if (!rows.length) { console.error('source form gansid-congress-2026 not found'); process.exit(1); }

const fields = rows[0].fields
  .filter((f) => !DROP.has(f.id))
  .map((f) => {
    // Strip pricing-only metadata — this form has no pricing template, so a
    // lingering usedForPricing flag would just be misleading dead config.
    const { usedForPricing, ...rest } = f;
    return { ...rest, required: KEEP_REQUIRED.has(f.id) ? true : false };
  });

const settings = {
  renderMode: 'single',
  successTitle: "You're registered 🎟️",
  submitButtonText: 'Complete my free registration',
};
const row = {
  id: 'gansid-congress-2026-invite',
  title: 'GANSID Congress 2026 — Free Registration',
  description: 'Confirm your free registration for the GANSID Congress 2026.',
  form_type: 'event',
  status: 'active',
  show_in_portal: false,
};

const lit = (o) => `'${JSON.stringify(o).replace(/'/g, "''")}'::jsonb`;
const s = (v) => `'${String(v).replace(/'/g, "''")}'`;

const sql = `-- Generated by scripts/seed-gansid-invite-form.mjs — DO NOT hand-edit.
-- Idempotent upsert of the GANSID free-registration form used by the invite path.
insert into public.forms (id, title, description, form_type, status, show_in_portal, fields, settings)
values (${s(row.id)}, ${s(row.title)}, ${s(row.description)}, ${s(row.form_type)}, ${s(row.status)}, ${row.show_in_portal}, ${lit(fields)}, ${lit(settings)})
on conflict (id) do update set
  title = excluded.title,
  description = excluded.description,
  form_type = excluded.form_type,
  status = excluded.status,
  show_in_portal = excluded.show_in_portal,
  fields = excluded.fields,
  settings = excluded.settings;
`;

fs.mkdirSync('supabase/seeds', { recursive: true });
fs.writeFileSync('supabase/seeds/gansid-invite-form.sql', sql, 'utf8');
console.log(`Wrote supabase/seeds/gansid-invite-form.sql — ${fields.length} fields; required: ${fields.filter((f) => f.required).map((f) => f.id).join(', ')}`);
